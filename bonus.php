<h1>Test Dumbways.id #5 Bonus Soal</h1>
<p>Soal : <br>
Anda ditunjuk sebagai CEO di suatu perusahaan yg based productnya adalah sabun batang. Mereka mempunyai kendala dengan mesin produksinya, yang mana mesinnya sometime tidak mengisikan sabun ke kardus, sehingga mengakibatkan ada beberapa kotak sabun yang kosong (BUG). Bagaimanakah cara Anda sebagai CEO untuk mengatasi problem tersebut?<br>
</p>

<p>Jawaban : <br>
Membuat AI (robot/mesin) untuk melakukan pengecekan <br>
Alasan :
Jika kasus ini problem hanya pada mesin yg sometime tidak mengisikan sabun ke kardus, saya memilih jawaban tersebut karena lebih efisien daripada solusi lain, sebab dengan teknologi AI (Artificial Inteligent) kita dapat memanfaatkan kecerdasaan buatan terhadap suatu objek (software/hardware) untuk membantu memberikan alert dan dapat menghentikan proses pada mesin tersebut</p>